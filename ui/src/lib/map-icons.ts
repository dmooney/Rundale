/** Name-keyword to icon mapping. Order matters: first match wins. */
const NAME_RULES: [RegExp, LocationIcon][] = [
	[/crossroads/i, 'signpost'],
	[/pub/i, 'beer-stein'],
	[/church/i, 'church'],
	[/letter\s*office/i, 'envelope'],
	[/school/i, 'book-open'],
	[/lough|shore/i, 'waves'],
	[/bay|harbour|harbor/i, 'anchor'],
	[/farm/i, 'barn'],
	[/fairy|fort|rath/i, 'sparkle'],
	[/bog|road/i, 'path'],
	[/shop/i, 'storefront'],
	[/kiln/i, 'fire'],
	[/village|town/i, 'house'],
	[/green|hurling/i, 'trophy']
];

/** Returns the icon key for a given location name. */
export function getLocationIcon(name: string): LocationIcon {
	for (const [pattern, icon] of NAME_RULES) {
		if (pattern.test(name)) return icon;
	}
	return 'map-pin';
}
